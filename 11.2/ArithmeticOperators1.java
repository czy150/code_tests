public class ArithmeticOperators1 {
  public static void main(String[] args) {
      // 声明两个整型变量 num1,num2 分别赋值为 2, 3
      int num1 = 2;
      float num2 = 3.45;
      // 使用加法运算符，对num1和num2执行相加操作，并将返回结果赋值给result
      float result = num1 + num2;
      System.out.println( num1 + " + " + num2 + " = " + result);
  }
}